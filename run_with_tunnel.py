import subprocess
import threading
import time
import socket
import re

PORT = 7860


# =============================
# WAIT WEBUI
# =============================
def wait_port(port):
    print("⏳ Waiting WebUI server...")
    while True:
        s = socket.socket()
        try:
            s.connect(("127.0.0.1", port))
            s.close()
            print("✅ WebUI detected!")
            break
        except:
            time.sleep(2)


# =============================
# OUTPUT READER (NON BLOCKING)
# =============================
def stream_output(proc, name, pattern):
    for line in iter(proc.stdout.readline, ''):
        line = line.strip()
        if not line:
            continue

        if pattern in line:
            url = re.search(r"(https://[^\s]+)", line)
            if url:
                print(f"✅ {name} URL:", url.group(1))
                break


# =============================
# CLOUDFLARE
# =============================
def start_cloudflare():
    print("🌐 Starting Cloudflare Tunnel...")

    import subprocess, time, requests, re

    subprocess.Popen(
        ["/usr/bin/cloudflared", "tunnel",
         "--url", f"http://127.0.0.1:{PORT}",
         "--no-autoupdate"],
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL
    )

    # tunggu cloudflare buat metrics server
    time.sleep(8)

    # ambil URL dari metrics API lokal
    for _ in range(15):
        try:
            r = requests.get("http://127.0.0.1:45678/metrics", timeout=3)
            m = re.search(r"https://[-a-z0-9]+\.trycloudflare\.com", r.text)
            if m:
                print("✅ Cloudflare URL:", m.group(0))
                return
        except:
            pass
        time.sleep(2)

    print("⚠️ Cloudflare tunnel aktif tapi URL belum terbaca")

# =============================
# PINGGY (TTY FIX + PARALLEL)
# =============================
def start_pinggy():
    print("🌐 Starting Pinggy Tunnel...")

    import subprocess, time

    subprocess.Popen([
        "ssh",
        "-tt",
        "-o", "StrictHostKeyChecking=no",
        "-o", "ServerAliveInterval=30",
        "-p", "443",
        f"-R0:localhost:{PORT}",
        "a.pinggy.io"
    ])

    # pinggy sekarang delay kirim URL → tunggu saja
    time.sleep(12)

    print("✅ Pinggy tunnel connected (URL biasanya muncul beberapa detik kemudian di backend)")
# =============================
# LOCALTUNNEL
# =============================
def start_localtunnel():
    print("🌐 Starting LocalTunnel...")

    p = subprocess.Popen(
        ["lt", "--port", str(PORT)],
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        bufsize=1
    )

    stream_output(p, "LocalTunnel", "loca.lt")


# =============================
# START WEBUI
# =============================
def start_webui():
    subprocess.Popen(
        ["python", "launch.py", "--listen", "--port", str(PORT)]
    )


# =============================
# MAIN
# =============================
print("🚀 Starting Stable Diffusion WebUI...")

threading.Thread(target=start_webui, daemon=True).start()

wait_port(PORT)

print("\n🚀 Starting tunnels...\n")

threading.Thread(target=start_cloudflare, daemon=True).start()
threading.Thread(target=start_pinggy, daemon=True).start()
threading.Thread(target=start_localtunnel, daemon=True).start()

# keep alive
while True:
    time.sleep(60)
