import subprocess
import threading
import time
import socket
import re

PORT = 7860


# =============================
# WAIT WEBUI READY
# =============================
def wait_port(port):
    print("⏳ Waiting WebUI server...")
    while True:
        s = socket.socket()
        try:
            s.connect(("127.0.0.1", port))
            s.close()
            print("✅ WebUI detected!")
            break
        except:
            time.sleep(2)


# =============================
# CLOUDFLARE
# =============================
def start_cloudflare():
    print("🌐 Starting Cloudflare Tunnel...")

    p = subprocess.Popen(
        ["cloudflared", "tunnel", "--url", f"http://127.0.0.1:{PORT}"],
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True
    )

    for line in p.stdout:
        if "trycloudflare.com" in line:
            url = re.search(r"(https://[-0-9a-zA-Z]+\.trycloudflare\.com)", line)
            if url:
                print(f"✅ Cloudflare URL: {url.group(1)}")
                break


# =============================
# PINGGY (FIXED)
# =============================
def start_pinggy():
    print("🌐 Starting Pinggy Tunnel...")

    cmd = [
        "ssh",
        "-o", "StrictHostKeyChecking=no",
        "-o", "ServerAliveInterval=30",
        "-p", "443",
        f"-R0:localhost:{PORT}",
        "a.pinggy.io"
    ]

    p = subprocess.Popen(
        cmd,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        bufsize=1
    )

    for line in iter(p.stdout.readline, ''):
        if "pinggy.link" in line or "https://" in line:
            print("✅ Pinggy URL:", line.strip())
            break
# =============================
# LOCALTUNNEL
# =============================
def start_localtunnel():
    print("🌐 Starting LocalTunnel...")

    p = subprocess.Popen(
        ["lt", "--port", str(PORT)],
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True
    )

    for line in p.stdout:
        if "your url is:" in line.lower():
            print("✅ LocalTunnel:", line.strip())
            break


# =============================
# START WEBUI
# =============================
def start_webui():
    subprocess.Popen(
        ["python", "launch.py", "--listen", "--port", str(PORT)]
    )


# =============================
# MAIN
# =============================
print("🚀 Starting Stable Diffusion WebUI...")

threading.Thread(target=start_webui).start()

wait_port(PORT)

print("\n🚀 Starting tunnels...\n")

threading.Thread(target=start_cloudflare, daemon=True).start()
threading.Thread(target=start_pinggy, daemon=True).start()
threading.Thread(target=start_localtunnel, daemon=True).start()

while True:
    time.sleep(60)
